#Pancari Keliling
print("Penghitung keliling")
panjang = input("Apa panjang persegi tsb? ") #Memasukkan panjang menggunakan input()
panjang = int(panjang) #menjadikan variabel panjang menjadi int, karena input menggunakan str
lebar = input("Apa lebar persegi tsb? ") #Memasukkan lebar menggunakan input()
lebar = int(lebar)
keliling = 2*(panjang + lebar) #Menghitung keliling persegi sesuai variabel panjang dan labar yang sudah dimasukkan oleh user/pengguna (dowo banget yo)
print("Kelilingnya adalah =", keliling)
